'''
Inicio aos operadores matematicos
'''
n1 = 3
n2 = 2.22

# Adição
adicao = n1 + n2
print('Adição:',adicao)
#multiplicação
mult = n1*n2
print('multiplicação:',mult)
# divisão (comum)
divisao = n1/n2
print('Divisão:', divisao)
# divisão inteira
divisao_inteira = n1 // n2
print('Divisão inteira: ',divisao_inteira)
# exponenciação
exponenciacao = n2**n1 
print('Exponenciação: ',exponenciacao)
#modulo
modulo = n1%n2 #resto da divisão
print('Modulo:', modulo)