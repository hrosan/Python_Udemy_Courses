'''
Concatenação e Repetição de strings


CONCATENAÇÃO > Para concatenar é utilizado o simbolo de '+' entre duas strings
REPETIÇÃO > Para repetir é utilizado o simbolo '*' e a quantidade de vezes a ser repetida
Ex: 'A'*10 -> Letra A será repetida 10 vezes
'''

nome = 'Henrique'
sobrenome = "Rosan"
#Concatenação
nome_completo = nome + " " + sobrenome
print(nome_completo)

#Repetição
repetir = nome*3
print(repetir)
