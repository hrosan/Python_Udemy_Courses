'''
In Python, a tuple is an ordered collection of elements enclosed in parentheses () or
without any enclosing brackets. Tuples are similar to lists, but they are immutable,
meaning their elements cannot be modified after creation. Tuples are commonly used to 
store related pieces of data as a single unit.
'''

# Here's an example of creating a tuple in Python:
fruits = ("apple", "banana", "orange")

'''
Tuples are useful in scenarios where you want to store a collection of values that should not be modified, such as coordinates, configuration settings, or database records.

It's important to note that the parentheses in a tuple are optional in most cases. 
The comma-separated values alone define a tuple. However, 
using parentheses can help make your code more readable and indicate the intention 
of creating a tuple.
'''