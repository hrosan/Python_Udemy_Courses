'''
Conversão de tipos, coersão
type convertion, typecasting, coercion
Tipos imutaveis e primitivos: str, int, float, bool
'''

# Soma
print(1+1)

# Concatenação
print('a'+'b') # concatenação, só se pode concatenar str com str

# Convertendo numeros/caracteres p/ outros tipos
print(int('1')+1) # Converte a string 1 p/ um valor inteiro e soma
print(str(1)+'1') # converte um int p/ uma string e concatena