'''
Tipos int e float
int > Numero inteiro
Tipo int representa qualquer numero, seja ele positivo ou negativo
Numero sem o sinal, será considerado positivo

Float > Numero com ponto flutuante, ou seja numeros Racionais ex: 3.1415, 9.81, etc...
O tipo float pode representar qualquer numero
Float sem sinal é considerado positivo

A função type() mostra o tipo de numero que o python inferiu o valor

'''
# Inteiros
print(5, 11, -15)

# Float
print(-2.15, 3.25, 0.444444)

# type
print(type('Henrique')) # retorna 'str'
print(type(1)) #retorna 'int'
